public class Operaciones {
    public void suma(int a,int b){
        int resultado = a + b;
        System.out.println(a + " + " + b + " = " + resultado);
    }   
    public void resta(int a, int b){
        int resultado = a - b;
        System.out.println(a + " + " + " = " + resultado);
    }
}
